								<p></p>
								<h5 style="color:#6F6F6F; text-align:center;">SEJARAH BERDIRINYA PEJABAT PENGELOLA INFORMASI DAN DOKUMENTASI (PPID) KABUPATEN WONOGIRI</h5>
								<p></p>
								<p style="text-align:justify">Sejak bergulirnya era reformasi pada 1998, telah mendorong terwujudnya sistem demokrasi di negeri ini.
									Gelombang demokratisasi telah membuka pintu kebebasan mengemukakan pendapat, kebebasan memperoleh informasi dan keadilan adalah prinsip yang harus ditegakkan.
									Keterbukaan informasi publik merupakan salah satu ciri negara demokratis yang menjunjung tinggi kedaulatan rakyat untuk mewujudkan penyelenggaraan negara yang baik dan bersih.
									Keterbukaan informasi merupakan sarana dalam mengoptimalkan pengawasan publik dan partisipasi publik terhadap penyelenggara negara dan Badan Publik lainnya yang menggunakan anggaran negara baik itu APBN maupun APBD.</p>
								<p style="text-align:justify">Lahirnya Undang-Undang Nomor 14 Tahun 2008 tentang Keterbukaan Informasi Publik, menjadi awal transparansi penyelenggaraan badan publik.
									Tujuan dari Undang-undang ini adalah untuk menjamin hak warga negara untuk mengetahui rencana pembuatan kebijakan publik, program kebijakan publik, dan proses pengambilan keputusan publik, serta alasan pengambilan suatu keputusan publik.
									Mendorong partisipasi masyarakat dalam proses pengambilan kebijakan publik, mewujudkan penyelenggaraan negara yang baik, yaitu yang transparan, efektif dan efisien, akuntabel serta dapat dipertanggungjawabkan.
									Mengetahui alasan kebijakan publik yang mempengaruhi hajat hidup orang banyak, mengembangkan ilmu pengetahuan dan mencerdaskan kehidupan bangsa serta meningkatkan pengelolaan dan pelayanan informasi di lingkungan Badan Publik untuk menghasilkan layanan informasi yang berkualitas.</p>
								<p style="text-align:justify">Sesuai dengan amanat tersebut, bahwa dalam rangka mewujudkan pelayanan informasi yang cepat, tepat, dan sederhana setiap Badan Publik wajib menunjuk Pejabat Pengelola Informasi dan Dokumentasi (PPID).
									Di Kabupaten Wonogiri, penunjukkan Pejabat Pengelola Informasi dan Dokumentasi ditetapkan dengan Keputusan Bupati Wonogiri Nomor 227 Tahun 2013.
									Dalam lampiran keputusan tersebut, Sekretaris Daerah Kabupaten Wonogiri menjabat sebagai Ketua Dewan Pertimbangan PPID, dengan Anggota Dewan Pertimbangan Asisten Pemerintahan Sekda Kabupaten Wonogiri, Asisten Perekonomian Pembangunan dan Kesejahteraan Rakyat Sekda Kabupaten Wonogiri serta Asisten Administrasi Sekda Kabupaten Wonogiri.
									Ketua PPID dijabat oleh Kepala Dinas Perhubungan Komunikasi dan Informatika Kabupaten Wonogiri. Dalam melaksanakan Keterbukaan Informasi Publik, PPID mengacu pada Undang-Undang RI No. 14 Tahun 2008 Tentang Keterbukaan Informasi Publik, Peraturan Pemerintah RI No 61 Tahun 2010 Tentang Pelaksanaan UU No. 14 Tahun 2008,
									Peraturan Komisi Informasi No. 01 Tahun 2010 Tentang Standart Layanan Informasi Publik, Peraturan Komisi Informasi No. 02 tahun 2010 tentang penyelesaian sengketa informasi serta Peraturan Daerah Kabupaten Wonogiri Nomor 1 Tahun 2014 Tentang Keterbukaan Informasi Publik.</p>
