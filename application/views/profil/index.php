								<p></p>
								<p style="text-align:center">“Membangun Wonogiri SUKSES, Beriman, Berbudaya, Berkeadilan, Berdaya Saing dan Demokratis”</p>
								<p></p>
								<h5 style="color:#6F6F6F;text-align:center;">PENJABARAN VISI</h5>
								<p></p>
								<p style="text-align:justify">Dalam rangka membangun dan mengelola pemerintahan di kabupaten Wonogiri di perlukan konsep pengelolaan dan manejemen pemerintahan yang baik,
									“SUKSES” dalam hal ini adalah sebuah motto Kerja Kabupaten Wonogiri yang telah ada sejak tahun 1986. Sukses bukan sekedar simbol atau Jargon Wonogiri
									tetapi lebih pada konsep manajemen pengelolaaan dan membangun Wonogiri dengan pengertian sebagai berikut:</p>
								<ol style="color:#6F6F6F;text-align:justify">
									<li>S singkatan dari Stabilitas, memiliki makna bahwa dalam membangun Wonogiri stabilitas merupakan salah satu prasyarat mutlak dalam melaksanakan pemerintahan,
										iklim yang kondusif baik di pemerintahan maupun di masyarakat adalah salah satu hal yang wajib ada;</li>
									<li>U singkatan dari Undang-undang, mengandung arti bahwa dalam menjalankan roda pemerintahan,
										melaksanakan pembagunan peraturan Per-Undang-undangan merupakan pijakan yuridis yang tak bisa di tawar;</li>
									<li>K singkatan dari Koordinasi, mengandung arti bahwa jika jalinan koordinasi dilakukan baik ke atas dalam hal ini pemerintah pusat dan provinsi,
										koordinasi kesamping dalam hal ini ke lembaga legislatif ataupun forum Koordinasi Pimpinan Daerah (Forkompimda) maupun koordinasi kebawah dalam hal ini kepada perangkat daerah dan masyarakat.
										Dari koordinasi ke atas diharapkan ada sinkronisasi program sekaligus harapan bisa mendatangkan bantuan yang lebih banyak dari pemerintah pusat untuk pembangunan Wonogiri,
										dari koordinasi kesamping diharapkan ada harmonisasi dan keselarasan dalam membuat kebijakan dengan lembaga legislatif dan dengan koordinasi kebawah akan tercipta partisipasi masyarakat dalam sumbang sihnya untuk kemajuan Wonogiri;</li>
									<li>S singkatan dari Sasaran, memiliki arti bahwa perencanaan dalam hal apapun harus jelas dan tepat sasaran sesuai dengan kebijakan-kebijakan yang telah di sepakati bersama dengan lembaga legislatif;</li>
									<li>E singkatan dari Evaluasi; mengandung makna bahwa setiap kegiatan dan program yang telah di laksanakan harus selalu di lakukan evaluasi guna peningkatan kualitas maupun kuantitas pada program tersebut.
										Evaluasi ini nantinya akan memerlukan sebuah keterbukaan terhadap kritik, kemauan untuk di kritik dan keinginan untuk mengkritik diri sendiri;</li>
									<li>S singkatan dari Semangat Juang, mengandung arti bahwa tanpa adanya semangat juang dari dalam diri kita maupun seluruh elemen penyelenggara pemerintahan mustahil program apapun tidak akan berjalan dengan baik.
										Sebagai pembawa amanah rakyat Wonogiri akan menempatkan diri sebagai motivator sekaligus obor guna membarakan semangat juang dalam membangun Wonogiri;</li>
									<li>BERIMAN, artinya bahwa masyarakat Wonogiri harus berTuhan dan memiliki keyakinan masing-masing sesuai dengan Agama yang di anut. Pemerintah Kabupaten Wonogiri akan mendorong semua kegiatan keagamaan,
										yang Islam laksanakan gerakan Wonogiri mengaji, yang Kristen, Katolik, Budha , Hindu, Konghucu dan aliran kepercayaan silahkan untuk menggagas gerakan, dan Pemerintah Kabupaten Wonogiri berkewajiban mendorongnya agar masyarakat Wonogiri Religius;</li>
									<li>BERBUDAYA, artinya bahwa mengembangkan budaya yang pernah ada dan tumbuh berkembang di masyarakat Wonogiri seperti tradisi dan adat di masing-masing dusun di wilayah Wonogiri,
										termasuk melestarikan budaya ketimuran saling menghormati menghargai dan menjunjung tinggi etika sopan santun di masyarakat;</li>
									<li>BERKEADILAN, artinya bahwa dalam bidang pembangunan, keagamaan dan lain-lain,
										Pemerintah Kabupaten Wonogiri akan berbuat seadil-adilnya bagi terciptanya kemakmuran bersama;</li>
									<li>BERDAYA SAING, artinya bahwa Wonogiri baik dari sisi pemerintahanya, orang- orangnya, potensi-potensinya harus memiliki daya saing yang tinggi dan sehat
										sehingga saling di perhitungkan baik dalam konteks pemerintahan maupun kualitas sumber daya manusianya yang menyangkut tingkat pendapatan, tingkat pendidikan dan derajat kesehatan masyarakat;</li>
									<li>DEMOKRATIS, memiliki makna bahwa dalam penyelenggaran umum pemerintahan di daerah sesuai dengan kewenangan dan urusan yang di miliki daerah harus dilaksanakan secara demokratis yang menjunjung tinggi nilai-nilai keberagaman,
										nilai-nilai demokrasi dan menjunjung tinggi hak dan kewajiban masyarakat.</li>
								</ol>
								</p>

