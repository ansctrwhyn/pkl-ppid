										<li>Terwujudnya pengelolaan sumber daya alam yang berwawasan lingkungan dan bekelanjutan.</li>
										<li>Terwujudnya ketercukupan dan pelayanan sarana prasarana yang berkualitas guna menunjang pengembangan wilayah.</li>
