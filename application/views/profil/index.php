									Mewujudkan Daerah Dan Masyarakat Wonogiri Yang Sejahtera , Bermartabat Dan Berdaya Saing Sebagai Landasan Bagi Tahap Pembangunan Berikutnya Menuju Masyarakat Adil Dan Makmur Dalam Negara Kesatuan Republik Indonesia Berdasarkan Pancasila Dan Undang- Undang Dasar Negra Republik Indonesia '1945.</p>
								<p style="text-align:justify">Sebagai ukuran terwujudnya Wonogiri yang sukses, sejahtera, demokratis, berdaya saing maka pembangunan jangka panjang Kabupaten Wonogiri diarahkan pada pencapaian sasaran-sasaran pokok sebagai berikut :
