										<li>Mengelola pemerintahan dengan membangun tata kelola pemerintahan yang bersih , efektif dan demokratis terpercaya yang meliputi unsur manajemen keuangan,
											manjemen pelayanan dan manajemen hukum dan pengawasan dengan semboyan SUKSES sebagai pola managerial yang memiliki makna sebagaimana penjelasan singkat dalam Visi;</li>
										<li>Meningkatkan kualitas hidup manusia Wonogiri melalui peningkatan kualitas pendidikan dan pelatihan dengan Program Wonogiri Pintar, peningkatan kesejahteraan masyarakat dengan program Wonogiri Kerja Wonogiri Sejahtera,
											peningkatan kualitas kesehatan dengan program Wonogiri Sehat serta Wonogiri beriman sesuai dengan agama dan keyakinan masing-masing mengedepankan sikap toleransi antar umat;</li>
										<li>Membangun dan memberdayakan Wonogiri dari pinggiran dengan memperkuat prioritas pembangunan di desa;</li>
										<li>Meningkatkan produktifitas rakyat Wonogiri dan daya saing di segala bidang sehingga Wonogiri dapat maju dan bangkit bersama daerah-daerah lain;</li>
										<li>Mengembangkan dan melestarikan adat dan budaya serta tradisi di masyarakat Wonogiri;</li>
										<li>Pemerataan pembangunan yang berkeadilan di segala bidang;</li>
										<li>Mengembangkan seluruh potensi-potensi didalam jiwa dan raganya Wonogiri untuk kemaslahatan rakyat Wonogiri.</li>
